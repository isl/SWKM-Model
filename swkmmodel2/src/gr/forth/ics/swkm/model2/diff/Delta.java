package gr.forth.ics.swkm.model2.diff;

import gr.forth.ics.swkm.model2.Triple;
import java.util.Collection;

/**
 * The result of applying the diff algorithm on two models.
 * Contains two sets of triples that are the added triples to the second version
 * and the deleted triples of the first version.
 * @author papavas
 */
public interface Delta {

    /**
     * Returns the triples that have been added in the second model
     * @return the set of added triples
     */
    Collection<Triple> getAddedSet();

    /**
     * Returns the triples that have been deleted in the second model
     * @return the set of deleted triples
     */
    Collection<Triple> getDeletedSet();

}
